// Función para obtener imágenes aleatorias de una categoría
export function getRandomImages(category: string, count: number = 4): string[] {
  const categoryImages = repuestoImages[category as keyof typeof repuestoImages] || repuestoImages['Accesorios'];
  const shuffled = [...categoryImages].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

// Función para obtener una imagen aleatoria de una categoría
export function getRandomImage(category: string): string {
  const categoryImages = repuestoImages[category as keyof typeof repuestoImages];
  if (!categoryImages || categoryImages.length === 0) {
    return repuestoImages['Accesorios'][0];
  }
  return categoryImages[Math.floor(Math.random() * categoryImages.length)];
}

